# Black-Scholes 公式
def norm_cdf(x):
    """标准正态分布累积分布函数"""
    return 0.5 * (1 + erf(x / np.sqrt(2)))

def bs_call_price(S, K, T, r, sigma, q=0):
    """
    Black-Scholes 看涨期权价格
    S: 标的价格
    K: 行权价
    T: 剩余期限（年）
    r: 无风险利率
    sigma: 波动率
    q: 股息率
    """
    if T <= 0 or sigma <= 0:
        return max(0, S - K)  # 到期时期权价值为内在价值
    
    d1 = (np.log(S / K) + (r - q + 0.5 * sigma**2) * T) / (sigma * np.sqrt(T))
    d2 = d1 - sigma * np.sqrt(T)
    
    call_price = S * np.exp(-q * T) * norm_cdf(d1) - K * np.exp(-r * T) * norm_cdf(d2)
    return call_price

def implied_volatility(market_price, S, K, T, r, q=0):
    """
    从市场价格反推隐含波动率
    使用 Brent 方法求解
    """
    if T <= 0:
        return None
    
    # 期权价格必须大于内在价值
    intrinsic_value = max(0, S - K)
    if market_price < intrinsic_value:
        return None
    
    # 波动率搜索范围：0.01 到 5.0（1% 到 500%）
    try:
        iv = brentq(
            lambda sigma: bs_call_price(S, K, T, r, sigma, q) - market_price,
            0.01, 5.0
        )
        return iv * 100  # 转换为百分比
    except:
        return None

